package P3;

public class Calculadora {

    // metodo que recebe dois inteiros soma e retorna como inteiro
    public int soma(int num1, int num2){
        return num1+num2;
    }

    // metodo que recebe dois double soma e retorna como double
    public double soma(double num1, double num2){
        return num1+num2;
    }
    
    // metodo que recebe tres float soma e retorna como float
    public float soma(float num1, float num2,float num3){
        return num1+num2+num3;
    }
}
